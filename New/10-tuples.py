# working with tuples:
# Tuples are immutable
# Tuples are ordered
# Tuples can contain any type of value
# Tuples can contain duplicates
# Tuples can be indexed
# Tuples can be sliced


# examples of how to use tuples in Python:
names = ("John", "Jane", "Joe")

# Access a value
print(names[0])

# Access a slice
print(names[0:2])

print(len(names))

print(names.count("John"))
print("Bob" in names)