# what is pip? pip is a package manager for python
# pip is used to install and manage software packages written in Python
# pypi.org is the official third-party software repository for Python

# commands:
# pip install <package>
# pip uninstall <package>
# pip freeze
# pip freeze > requirements.txt
# pip install -r requirements.txt
# pip install --upgrade <package>

# pip install requests - it is used to make HTTP requests
# pip install -U requests - upgrade requests
# pip uninstall requests - uninstall requests
# pip show requests - show information about requests
